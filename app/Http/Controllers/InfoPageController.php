    public function show(Request $request, string $slug): View
    {
        $pages = $this->pages();

        if (! array_key_exists($slug, $pages)) {
            abort(404);
        }

        $page = $pages[$slug];

        $doctorsQuery = Doctor::query()->with('user', 'services')->orderBy('id');

        $doctors = match ($slug) {
            'therapy' => $doctorsQuery
                ->where('specialization', 'like', '%Терапев%')
                ->get(),
            'diagnostics' => $doctorsQuery
                ->where(function ($q) {
                    $q->where('specialization', 'like', '%УЗИ%')
                        ->orWhere('specialization', 'like', '%КТ%')
                        ->orWhere('specialization', 'like', '%МРТ%')
                        ->orWhere('specialization', 'like', '%Рентген%')
                        ->orWhere('specialization', 'like', '%Лучевая%')
                        ->orWhere('specialization', 'like', '%Функциональ%');
                })
                ->get(),
            'narrow-directions' => $doctorsQuery->get(),
            'online-booking' => $doctorsQuery->get(),
            default => $doctorsQuery->get(),
        };

        $specializations = null;
        if ($slug === 'narrow-directions') {
            $specializations = $doctors
                ->pluck('specialization')
                ->filter()
                ->map(fn ($s) => trim((string) $s))
                ->filter()
                ->unique()
                ->sort()
                ->values();
        }

        return view('info.show', [
            'slug' => $slug,
            'page' => $page,
            'doctors' => $doctors,
            'specializations' => $specializations,
        ]);
    }

